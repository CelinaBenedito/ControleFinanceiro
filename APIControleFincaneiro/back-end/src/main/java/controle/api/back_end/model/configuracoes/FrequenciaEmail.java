package controle.api.back_end.model.configuracoes;

/**
 * Frequência com que o usuário deseja receber relatórios de poupança por e-mail.
 */
public enum FrequenciaEmail {
    /** Um e-mail toda segunda-feira com o resumo semanal. */
    SEMANAL,

    /** Um e-mail no 1º dia de cada mês com o resumo mensal. */
    MENSAL
}

